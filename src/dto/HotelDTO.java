package dto;

public class HotelDTO {
	private String hotelNo;		// 호텔번호
	private String hotelNm;		// 호텔명
	private String hotelDc;		// 호텔설명
	private String hotelTelno;	// 호텔전화번호
	private String hotelLqtx;	// 호텔주소
	private String photoCours;	// 사진경로
	
	
	
	public String getHotelNo() {
		return hotelNo;
	}
	public void setHotelNo(String hotelNo) {
		this.hotelNo = hotelNo;
	}
	public String getHotelNm() {
		return hotelNm;
	}
	public void setHotelNm(String hotelNm) {
		this.hotelNm = hotelNm;
	}
	
	public String getHotelDc() {
		return hotelDc;
	}
	public void setHotelDc(String hotelDc) {
		this.hotelDc = hotelDc;
	}
	public String getHotelTelno() {
		return hotelTelno;
	}
	public void setHotelTelno(String hotelTelno) {
		this.hotelTelno = hotelTelno;
	}
	public String getHotelLqtx() {
		return hotelLqtx;
	}
	public void setHotelLqtx(String hotelLqtx) {
		this.hotelLqtx = hotelLqtx;
	}
	public String getPhotoCours() {
		return photoCours;
	}
	public void setPhotoCours(String photoCours) {
		this.photoCours = photoCours;
	}
	
	
	
}
